#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	size_t	i;

	i = 0;
	while (s[i])
	{
		ft_putchar_fd(s[i], fd);
		i++;
	}
}

int main(int ac, char **av)
{
	ft_putstr_fd(av[1], atoi(av[2]));
}
